const uint8_t* Get_Plane_Sprite(float p, float r, float y) {
    float yaw_norm = fmod(y, 360.0);
    if (yaw_norm > 180.0) yaw_norm -= 360.0;
    if (yaw_norm < -180.0) yaw_norm += 360.0;

    int is_up = (p >= 0.0);

    if (yaw_norm > -22.5 && yaw_norm <= 22.5)   return is_up ? NU  : ND;
    if (yaw_norm > 22.5 && yaw_norm <= 67.5)    return is_up ? NEU : NED;
    if (yaw_norm > 67.5 && yaw_norm <= 112.5)   return is_up ? EU  : ED;
    if (yaw_norm > 112.5 && yaw_norm <= 157.5)  return is_up ? SEU : SED;
    if (yaw_norm > 157.5 || yaw_norm <= -157.5) return is_up ? SU  : SD;
    if (yaw_norm > -157.5 && yaw_norm <= -112.5)return is_up ? SWU : SWD;
    if (yaw_norm > -112.5 && yaw_norm <= -67.5) return is_up ? WU  : WD;
    if (yaw_norm > -67.5 && yaw_norm <= -22.5)  return is_up ? NWU : NWD;

    return NU;
}
